package com.dw.model;

public class testTree {
	private static final long serialVersionUID = 1L;
	private String id;
	private String name;
	private String value;
	private String doneCount;
	private String unDoneCount;
	private String processingRate;
	
	
	@Override
	public String toString() {
		return "testTree [id=" + id + ", name=" + name + ", value=" + value + ", doneCount=" + doneCount
				+ ", unDoneCount=" + unDoneCount + ", processingRate=" + processingRate + "]";
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getDoneCount() {
		return doneCount;
	}
	public void setDoneCount(String doneCount) {
		this.doneCount = doneCount;
	}
	public String getUnDoneCount() {
		return unDoneCount;
	}
	public void setUnDoneCount(String unDoneCount) {
		this.unDoneCount = unDoneCount;
	}
	public String getProcessingRate() {
		return processingRate;
	}
	public void setProcessingRate(String processingRate) {
		this.processingRate = processingRate;
	}
	

}
