package com.dw.model;

public class CityCode {

 	private String prov_id;
	private String prov_name;
	private String latn_id;
	private String latn_name;
	private String south_north;
	private String region_type;
	private String area_code;
	
	 
	public String getProv_id() {
		return prov_id;
	}
	public void setProv_id(String prov_id) {
		this.prov_id = prov_id;
	}
	public String getProv_name() {
		return prov_name;
	}
	public void setProv_name(String prov_name) {
		this.prov_name = prov_name;
	}
	public String getLatn_id() {
		return latn_id;
	}
	public void setLatn_id(String latn_id) {
		this.latn_id = latn_id;
	}
	public String getLatn_name() {
		return latn_name;
	}
	public void setLatn_name(String latn_name) {
		this.latn_name = latn_name;
	}
	public String getSouth_north() {
		return south_north;
	}
	public void setSouth_north(String south_north) {
		this.south_north = south_north;
	}
	public String getRegion_type() {
		return region_type;
	}
	public void setRegion_type(String region_type) {
		this.region_type = region_type;
	}
	public String getArea_code() {
		return area_code;
	}
	public void setArea_code(String area_code) {
		this.area_code = area_code;
	}
	
	
	
}
